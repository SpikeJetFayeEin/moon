def _build_nav(start: float, daily_changes: list[float]) -> list[dict]:
    nav = start
    base = date.today() - timedelta(days=len(daily_changes))
    points = []
    for index, change in enumerate(daily_changes):
        nav = round(nav * (1 + change), 4)
        points.append(
            {
                "date": base + timedelta(days=index + 1),
                "nav": nav,
                "accumulated_nav": nav,
            }
        )
    return points


